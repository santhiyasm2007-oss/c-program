#include <stdio.h>

int findGCD(int a, int b) {
    while (b != 0) {
        int temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}
int main() {
    int A, B;
    scanf("%d %d", &A, &B);
    int gcd = findGCD(A, B);
    printf("%d", gcd);
    return 0;
}
